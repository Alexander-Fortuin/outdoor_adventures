// Camping
// image 1
function toggle1() {
  var click = document.querySelector('.click');
  var over = document.querySelector('.box1');
  if (over.className === 'box1 down') {
    over.className = 'box1 up';
  } else {
    over.className = 'box1 down';
  }
}
